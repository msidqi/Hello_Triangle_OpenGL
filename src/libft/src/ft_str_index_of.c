#include "libft.h"

ssize_t		ft_str_index_of(const char *s, char c)
{
	ssize_t	i;

	i = -1;
	while (s[++i])
	{
		if (s[i] == c)
			return (i);
	}
	if (c == '\0')
		return (i);
	return (-1);
}
